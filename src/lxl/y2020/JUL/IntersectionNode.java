public class IntersectionNode {
    class ListNode {
        int val;
        ListNode next;

        ListNode(int x) {
            val = x;
            next = null;
        }
    }

    public ListNode getIntersectionNode(ListNode headA, ListNode headB) {
        Set<ListNode> aExsit = new HashSet<>();
        Set<ListNode> bExsit = new HashSet<>();
        while (headA != null || headB != null) {
            if (headA != null) {
                if (!bExsit.contains(headA)) {
                    aExsit.add(headA);
                    headA = headA.next;
                } else {
                    return headA;
                }
            }
            if (headB != null) {
                if (!aExsit.contains(headB)) {
                    bExsit.add(headB);
                    headB = headB.next;
                } else {
                    return headB;
                }
            }
        }
        return null;
    }

    //解题思路：
    //
    //我们使用两个指针 node1，node2 分别指向两个链表 headA，headB 的头结点，然后同时分别逐结点遍历，当 node1 到达链表 headA 的末尾时，
    //重新定位到链表 headB 的头结点；当 node2 到达链表 headB 的末尾时，重新定位到链表 headA 的头结点。
    //
    //这样，当它们相遇时，所指向的结点就是第一个公共结点。
    //a+b长度一致
    public ListNode getIntersectionNode2(ListNode headA, ListNode headB) {
        ListNode nodeA = headA;
        ListNode nodeB = headB;
        while (nodeA != nodeB) {
            nodeA = nodeA == null ? headB : nodeA.next;
            nodeB = nodeB == null ? headA : nodeB.next;
        }
        return nodeA;
    }
}
