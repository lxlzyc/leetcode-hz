public class MiddleOfTheLinkedList {
    static class ListNode {
        int val;
        ListNode next;

        ListNode(int x) {
            val = x;
        }
    }

    public ListNode middleNode(ListNode head) {
        ListNode offset1 = head;
        ListNode offset2 = head;
        while (offset2 != null) {
            offset2 = offset2.next;
            if (offset2 != null) {
                offset2 = offset2.next;
            } else {
                break;
            }
            offset1 = offset1.next;
        }
        return offset1;
    }

    public static void main(String[] args) {
        MiddleOfTheLinkedList middleOfTheLinkedList = new MiddleOfTheLinkedList();
        ListNode node1 = new ListNode(1);
        ListNode node2 = new ListNode(2);
        ListNode node3 = new ListNode(3);
        ListNode node4 = new ListNode(4);
        ListNode node5 = new ListNode(5);

        node1.next = node2;
        node2.next = node3;
        node3.next = node4;
        //node4.next = node5;
        System.out.println(middleOfTheLinkedList.middleNode(node1));
    }

}
